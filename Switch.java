package Excel;

public class Switch {

	public static void main(String args[])
	{
		int number = 10;
		switch(number)
		{
		
		case 10 : 
			System.out.println("Number is " + number);
			break;
		case 20 :
		System.out.println("Number is " + number);
		break;
		
		case 30 : 
			System.out.println("Number is " + number);
			
			break;
			
			default:
				System.out.println("Number is not available");
		}
		
	}
}
